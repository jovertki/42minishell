#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*ptr;
	unsigned char	uc;

	i = 0;
	ptr = (unsigned char *)s;
	uc = (unsigned char)c;
	while (n--)
	{
		if (ptr[i] == uc)
			return (&ptr[i]);
		i++;
	}
	return (NULL);
}
